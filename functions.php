<?php

function user_sprintf(...$args) {
	return sprintf(...$args);
}

function user_vsprintf(...$args) {
	return vsprintf(...$args);
}

function user_printf(...$args) {
	return printf(...$args);
}

function user_vprintf(...$args) {
	return vprintf(...$args);
}

function user_fprintf(...$args) {
	return fprintf(...$args);
}

function user_vfprintf(...$args) {
	return vfprintf(...$args);
}

?>